class UsersController < ApplicationController
  http_basic_authenticate_with :name => "test", :password => "test", :except => :new, :create
  before_action :set_user, only: [:show, :edit, :update, :destroy]

  # GET /users
  # GET /users.json
  def index
    @users = User.all
  end

  # GET /users/1
  # GET /users/1.json
  def show
  end

  # GET /users/new
  def new
    @user = User.new
  end

  # GET /users/1/edit
  def edit
  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(user_params)

    respond_to do |format|
      if @user.save

        account_sid = ENV['TWILIO_SID']
        auth_token = ENV['TWILIO_AUTH_TOKEN']

        client = Twilio::REST::Client.new account_sid, auth_token

        from = ENV['TWILIO_NUMBER']
        body = "You've been added to the AFR SMS system. Messages sent to: #{from} will be broadcast to the group. Please save this number!"

        client.account.messages.create(
          :from => from,
          :to => @user.phone_number,
          :body => body
        )

        format.html { redirect_to @user, notice: 'Your number has been added!' }
        format.json { render action: 'show', status: :created, location: @user }
      else
        format.html { render action: 'new' }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /users/1
  # PATCH/PUT /users/1.json
  def update
    respond_to do |format|
      if @user.update(user_params)
        format.html { redirect_to @user, notice: 'User was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: 'edit' }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    @user.destroy
    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_user
      @user = User.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def user_params
      params.require(:user).permit(:phone_number)
    end
end
